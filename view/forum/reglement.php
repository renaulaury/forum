        </p>

        <ul>
            <li><strong>Harcèlement ou menaces</strong> : Comportements répétitifs visant à harceler ou menacer un utilisateur.</li>
            <li><strong>Contenus illégaux</strong> : Publication de contenus interdits par la loi (drogues, violence extrême, etc.).</li>
            <li><strong>Usurpation d’identité</strong> : Création de comptes ou contenus imitant un autre utilisateur ou une autorité (modérateur, administrateur).</li>
            <li><strong>Répétition d'infractions graves</strong> : Répétition d'infractions après plusieurs avertissements et bannissements temporaires.</li>
            <li><strong>Violation grave des droits d’auteur</strong> : Partage de contenus protégés à grande échelle sans autorisation et malgré des avertissements.</li>
            <li><strong>Diffusion de contenus dangereux</strong> : Publication de contenus encourageant la violence, l'auto-harmonie, ou d’autres comportements destructeurs.</li>
        </ul>
