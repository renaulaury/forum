        <h1>Règlement</h1>

        <ul>
            <li><strong>Respectez les autres membres</strong> : Soyez courtois et respectueux dans vos échanges. Les insultes et comportements agressifs ne seront pas tolérés.</li>
            <li><strong>Pas de contenu offensant</strong> : Il est interdit de publier du contenu haineux, discriminatoire ou choquant.</li>
            <li><strong>Respect de la vie privée</strong> : Ne partagez pas d'informations personnelles sensibles sans le consentement des personnes concernées.</li>
            <li><strong>Pas de spams</strong> : La publicité non sollicitée, le spam ou les liens vers des sites malveillants sont interdits.</li>
            <li><strong>Utilisation d'un langage approprié</strong> : Évitez les propos vulgaires ou inappropriés. Le forum est un espace de discussion civilisée.</li>
            <li><strong>Respect des droits d'auteur</strong> : Ne partagez pas de contenu protégé par des droits d'auteur sans permission ou attribution appropriée.</li>
            <li><strong>Modération</strong> : Les administrateurs sont là pour faire respecter ces règles. Leur décision est finale. Si vous enfreignez ces règles, des sanctions peuvent être appliquées, y compris un bannissement temporaire ou permanent.</li>
        </ul>
    </div>

    <div class="banTemporaire">
        <h2>Infractions justifiant un bannissement temporaire (3 jours)</h2>

        <ul>
            <li><strong>Insultes ou propos offensants</strong> : Usage de langage injurieux envers un utilisateur ou un groupe. </li>
            <li><strong>Spam ou publicité non autorisée</strong> : Publication répétée de contenus promotionnels ou non pertinents. </li>
            <li><strong>Perturbation intentionnelle</strong> : Flood, trolling ou actions visant à déstabiliser les discussions. </li>
            <li><strong>Non-respect des règles de contenu</strong> : Partage de contenus inappropriés (violence, pornographie, etc.). </li>
            <li><strong>Manque de respect envers les administrateurs</strong> : Opposition ou contestation agressive des décisions des administrateurs. </li>
            <li><strong>Non-respect des droits d’auteur</strong> : Publication de contenus protégés sans autorisation. </li>
        </ul>
    </div>

