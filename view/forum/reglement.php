            <li><strong>Pas de spams</strong> : La publicité non sollicitée, le spam ou les liens vers des sites malveillants sont interdits.</li>
            <li><strong>Utilisation d'un langage approprié</strong> : Évitez les propos vulgaires ou inappropriés. Le forum est un espace de discussion civilisée.</li>
            <li><strong>Respect des droits d'auteur</strong> : Ne partagez pas de contenu protégé par des droits d'auteur sans permission ou attribution appropriée.</li>
            <li><strong>Modération</strong> : Les modérateurs sont là pour faire respecter ces règles. Leur décision est finale. Si vous enfreignez ces règles, des sanctions peuvent être appliquées, y compris un bannissement temporaire ou permanent.</li>
        </ul>

        <p>Merci de votre compréhension et de votre collaboration. Nous vous souhaitons une agréable expérience sur le forum !</p>
